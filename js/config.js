// ===== دوال =====
function getCategory(word) {
  for (const cat in CATEGORIES) {
    if (CATEGORIES[cat].includes(word)) return cat;
  }
  return null;
}

function addSynonym(word, synonym) {
  if (!SYNONYMS[word]) SYNONYMS[word] = [];
  if (!SYNONYMS[word].includes(synonym)) SYNONYMS[word].push(synonym);
}

function getSynonyms(word) {
  return SYNONYMS[word] || [];
}

function normalizeRelation(rel) {
  let cleaned = rel.replace(/ على| من| في| لل| إلى| عن/g, '').trim();
  if (cleaned.startsWith('ي') || cleaned.startsWith('ت')) {
    cleaned = cleaned.slice(1);
  }
  return cleaned;
}

function getStem(w) {
  if (typeof window.expertMode !== 'undefined' && window.expertMode && typeof qutuf !== 'undefined') {
    try { const r = qutuf(w); if (r && r.root) return r.root; } catch (_) {}
  }
  return VERB_ROOTS[w] || w;
}

function calculateConfidence(subj, rel, obj) {
  const facts = window.brain?.facts?.[subj]?.[rel];
  if (!facts || !facts.includes(obj)) return 0;

  const count = facts.filter(f => f === obj).length;
  const totalRels = Object.values(window.brain?.facts?.[subj] || {}).reduce((sum, arr) => sum + arr.length, 0);

  let confidence = totalRels > 0 ? Math.round((count / totalRels) * 100) : 50;
  if (count > 1) confidence = Math.min(100, confidence + (count * 5));
  const category = getCategory(subj);
  if (category) confidence = Math.min(100, confidence + 10);
  confidence = Math.min(100, Math.max(40, confidence));

  return confidence;
}

